    public void part2() {
        ArrayList<Integer> left = new ArrayList<>();
        ArrayList<Integer> right = new ArrayList<>();
        String[] lines = input.split("\n");
        for (String line : lines) {
            String[] values = line.split(" {3}");
            left.add(Integer.parseInt(values[0]));
            right.add(Integer.parseInt(values[1]));
        }

        Map<Integer, Integer> appearences = new HashMap<>();
        for (Integer k : left) {
            Integer v = appearences.get(k);
            if (v == null) {
                appearences.put(k, 1);
            } else {
                appearences.put(k, ++v);
            }
        }
        BigDecimal total = BigDecimal.ZERO;
        for (Integer k : right) {
            if (appearences.get(k) != null) {
                BigDecimal number = new BigDecimal(k * appearences.get(k));
                total = total.add(number);
            }
        }
        System.out.println("Day one Part 2 total");
        System.out.println(total);
    }
