    ArrayList<Equation> equations;

    public void read() {
        equations = new ArrayList<>();
        String[] lines = input.split("\n");
        for (String line : lines) {
            String[] sides = line.split(":");
            BigInteger total = BigInteger.valueOf(Long.parseLong(sides[0]));
            String[] right = sides[1].trim().split(" ");
            ArrayList<BigInteger> values = new ArrayList<>();
            for (String value : right) {
                BigInteger val = BigInteger.valueOf(Long.parseLong(value));
                values.add(val);
            }
            equations.add(new Equation(total, values));
        }
    }

