    public void read() {
        locks = new ArrayList<>();
        keys = new ArrayList<>();
        String[] lines = input.split("\n");
        for (int i = 0; i < lines.length; i += 8) {
            int[] pins = new int[5];
            for (int j = 0; j < 5; j++) {
                pins[j] = 5;
            }
            String[] l = new String[7];
            l[0] = lines[i];
            l[1] = lines[i + 1];
            l[2] = lines[i + 2];
            l[3] = lines[i + 3];
            l[4] = lines[i + 4];
            l[5] = lines[i + 5];
            l[6] = lines[i + 6];
            boolean lock = true;
            if (l[0].charAt(0) == '.') {
                lock = false;
            }
            if (lock) {
                for (int lineNumber = 1; lineNumber < 6; lineNumber++) {
                    for (int charPosition = 0; charPosition < 5; charPosition++) {
                        if (l[lineNumber].charAt(charPosition) == '.') {
                            pins[charPosition]--;
                        }
                    }
                }
                locks.add(new Schematic(pins, lock));
            } else {
                for (int lineNumber = 6; lineNumber >= 1; lineNumber--) {
                    for (int charPosition = 0; charPosition < 5; charPosition++) {
                        if (l[lineNumber].charAt(charPosition) == '.') {
                            pins[charPosition]--;
                        }
                    }
                }
                keys.add(new Schematic(pins, lock));
            }
        }
    }

